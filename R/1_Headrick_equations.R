# Headrick 12 (p. 689)
# calculates the normalized third cumulant

f_skew <- function(x) {
  sd.x <- stats::sd(x)
  mu3.x <- mean((x - mean(x))^3)
  skew <- mu3.x / sd.x^3

  return(skew)
}

# Headrick 13 (p. 689)
# calculates the normalized fourth cumulant

f_kurt <- function(x) {
  sd.x <- stats::sd(x)
  mu4 <- mean((x - mean(x))^4)
  kurt <- mu4 / sd.x^4 - 3

  return(kurt)
}

# Headrick 14 (p. 690)
# calculates the normalized fifth cumulant

f_gamma3 <- function(x) {
  sd.x <- stats::sd(x)
  mu3 <- mean((x - mean(x))^3)
  mu5 <- mean((x - mean(x))^5)
  gamma3 <- mu5 / sd.x^5 - 10 * mu3 / sd.x^3

  return(gamma3)
}

# Headrick 15 (p. 690)
# calculates the normalized sixth cumulant

f_gamma4 <- function(x) {
  sd.x <- stats::sd(x)
  mu3 <- mean((x - mean(x))^3)
  mu4 <- mean((x - mean(x))^4)
  mu6 <- mean((x - mean(x))^6)
  gamma4 <- mu6 / sd.x^6 - 15 * mu4 / sd.x^4 + 45 - 10 * mu3^2 / sd.x^3 - 15

  return(gamma4)
}

